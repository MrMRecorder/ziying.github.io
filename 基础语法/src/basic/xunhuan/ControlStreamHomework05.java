package basic.xunhuan;

public class ControlStreamHomework05 {

    public static void main(String[] args) {

        //字母表
        int a = 97;
        for (int i = 0; i < 26; i++) {
            System.out.print((char) a+"\t");
            a++;
        }
        System.out.println();
        int z = 90;
        for (int j = 0; j < 26; j++) {
            System.out.print((char) z+"\t");
            z--;
        }


    }


}
